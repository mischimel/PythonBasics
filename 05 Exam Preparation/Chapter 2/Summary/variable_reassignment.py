# Variable Reassignment


# Variables can reference different values while the program is running
age = 25

# Reassigning the variable 'age' to a new value
age = 30    # 'age' is no longer referencing the value 25
            # Garbage collection: removal of values that are no longer referenced by variables

# A variable can refer to an item of any type
message = "Hello"

# Reassigning the variable 'message' to a different type
message = 42 # Variable that has been assigned to one type can be reassigned to another type




