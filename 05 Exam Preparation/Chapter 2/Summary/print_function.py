# print Function

#*Basic Usage of print:
# Using the print function to display a string
print("Hello, World!")

#*Printing Multiple Items:
# Displaying multiple items with a single call to print
name = "Alice"
age = 30
print("Name:", name, "Age:", age)

#*Automatic Space Separation:
# Automatic space separation when displaying multiple items
city = "New York"
state = "NY"
print("City:", city, "State:", state)

#*Expressions as Arguments:
# Using expressions as arguments to print
x = 5
y = 10
print("Sum:", x + y)

#*Statements Execute in Order:
# Statements in a program execute in the order they appear
print("First statement")
print("Second statement")
