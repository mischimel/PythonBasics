# Assuming there are no accidents or delays, the distance that a car travels down
# the interstate can be calculated with the following formula:
# Distance = Speed * Time
# A car is traveling at 70 miles per hour.
# Write a program that displays the following:
#   • The distance the car will travel in 6 hours
#   • The distance the car will travel in 10 hours
#   • The distance the car will travel in 15 hours

# Calulate the distances
dristance1 = 70 * 6
dristance2 = 70 * 10
dristance3 = 70 * 15

# Print the distances
print(f"The distance the car will travel in 6 hours is {dristance1} miles")
print(f"The distance the car will travel in 10 hours is {dristance2} miles")
print(f"The distance the car will travel in 15 hours is {dristance3} miles")
